#!/usr/bin/env node
// Generates BOOSTERS.md — a human-readable at-a-glance summary of every
// per-set booster recipe in data/booster-contents, so gaps/oddities are
// easy to spot. Regenerate after editing recipes:
//   node scripts/dump-booster-summary.mjs
//
// Set names + release dates are pulled from a cached Scryfall set list if
// one is present (/tmp/scry_sets.json), else parsed from each file's _doc,
// else the bare code is shown.

import { promises as fs } from "fs";
import path from "path";

const ROOT = process.cwd();
const DIR = path.join(ROOT, "data", "booster-contents");
const OUT = path.join(ROOT, "BOOSTERS.md");
const PT_ORDER = ["play", "draft", "collector", "jumpstart"];

async function loadNameMap() {
  const map = {};
  try {
    const j = JSON.parse(await fs.readFile("/tmp/scry_sets.json", "utf-8"));
    for (const s of j.data) map[s.code] = { name: s.name, released: s.released_at };
  } catch {}
  return map;
}

function nameFromDoc(doc, code) {
  if (!doc) return code.toUpperCase();
  // "Foo Bar (CODE) ..." or "Foo Bar — ..." or "Foo Bar booster ..."
  const m = doc.match(/^(.+?)\s*(?:\(|—|booster|recipe|—)/i);
  return (m ? m[1] : doc.slice(0, 40)).trim();
}

// A compact per-pack view: the slot list (label + count) plus the distinct
// special treatments (filters) and bonus sub-sets the pack can pull from.
function summarizePack(pack, ownCode) {
  const slots = [];
  const treatments = new Set();
  const sets = new Set();
  for (const slot of pack.slots) {
    const cnt = slot.count ?? 1;
    slots.push(cnt > 1 ? `${cnt}× ${slot.label}` : slot.label);
    for (const o of slot.outcomes) {
      if (o.filter) treatments.add(o.filter);
      if (o.set && o.set !== "$tokens" && o.set.toLowerCase() !== ownCode) sets.add(o.set.toUpperCase());
    }
  }
  return {
    cardCount: pack.cardCount,
    slots: slots.join(", "),
    treatments: [...treatments].sort(),
    sets: [...sets].sort(),
  };
}

async function main() {
  const names = await loadNameMap();
  const files = (await fs.readdir(DIR)).filter((f) => f.endsWith(".json") && f !== "default.json");
  const rows = [];
  for (const f of files) {
    const code = f.replace(/\.json$/, "");
    const d = JSON.parse(await fs.readFile(path.join(DIR, f), "utf-8"));
    const meta = names[code] || {};
    rows.push({
      code,
      name: meta.name || nameFromDoc(d._doc, code),
      released: meta.released || "",
      authoritative: !!d.authoritative,
      pack: d,
    });
  }
  // Sort newest first when we have dates, else alphabetical by name.
  rows.sort((a, b) => (b.released || "").localeCompare(a.released || "") || a.name.localeCompare(b.name));

  const lines = [];
  lines.push("# Booster contents — at a glance");
  lines.push("");
  lines.push("_Auto-generated by `scripts/dump-booster-summary.mjs` from `data/booster-contents/`. Do not edit by hand._");
  lines.push("");
  lines.push(`${rows.length} sets with a per-set recipe. Pack types shown are exactly what each set offers (sets marked **[authoritative]** hide every other type; others also fall back to the date heuristic).`);
  lines.push("");
  for (const r of rows) {
    const head = `## ${r.name} \`${r.code}\`${r.released ? ` · ${r.released}` : ""}${r.authoritative ? " · **[authoritative]**" : ""}`;
    lines.push(head);
    for (const pt of PT_ORDER) {
      if (!r.pack[pt]) continue;
      const s = summarizePack(r.pack[pt], r.code);
      const label = pt[0].toUpperCase() + pt.slice(1);
      let line = `- **${label}** (${s.cardCount} cards): ${s.slots}`;
      if (s.treatments.length) line += ` · treatments: ${s.treatments.join(", ")}`;
      if (s.sets.length) line += ` · bonus sets: ${s.sets.join(", ")}`;
      lines.push(line);
    }
    lines.push("");
  }
  await fs.writeFile(OUT, lines.join("\n"), "utf-8");
  console.log(`Wrote ${OUT} (${rows.length} sets).`);
}

main().catch((e) => { console.error(e); process.exit(1); });
